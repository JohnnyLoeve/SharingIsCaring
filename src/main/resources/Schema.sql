CREATE TABLE Meal(
  id BIGINT AUTO_INCREMENT PRIMARY KEY,
  name VARCHAR(64) NOT NULL,
  description VARCHAR(MAX),
  price BIGINT NOT NULL,
  link VARCHAR(MAX)

);

CREATE TABLE USER_PROFILE(
id BIGINT AUTO_INCREMENT PRIMARY KEY,
name VARCHAR(64) NOT NULL,
username VARCHAR(64),
password VARCHAR(64)
email VARCHAR(64),
address VARCHAR(64),
phone VARCHAR(64),
country VARCHAR(64),
postcode VARCHAR(64),
);